package geoviz;

public class MyPoint {

    private double x;
    private double y;

    public MyPoint(double xCoordinate, double yCoordinate){
        this.x = xCoordinate;
        this.y = yCoordinate;
    }

    public double getX(){
        return x;
    }

    public double getY(){
        return y;
    }

}
